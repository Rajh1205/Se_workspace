#include <iostream>
using namespace std;

int main() {
    int num1, num2, sum;

    // Taking input from the user
    cout << "Enter first number: ";
    cin >> num1;

    cout << "Enter second number: ";
    cin >> num2;

    // Calculating sum
    sum = num1 + num2;

    // Displaying result
    cout << "Sum of the two numbers = " << sum;

    return 0;
}
